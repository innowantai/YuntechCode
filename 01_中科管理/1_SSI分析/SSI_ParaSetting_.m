% --- Executes on button press in btnSave.
function btnSave_Callback(hObject, eventdata, handles)
% hObject    handle to btnSave (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
load('para')
dt = str2num( get(handles.txtHz,'string') );
timeLength =str2num( get(handles.txtLength,'string'));
iint =str2num( get(handles.txtImin,'string'));
imax =str2num( get(handles.txtImax,'string'));
n3 =str2num( get(handles.txtN,'string'));
alpha =str2num( get(handles.txtAlpha,'string'));
deltaf =str2num( get(handles.txtDeltaf,'string'));
plotopen = str2num(get(handles.txtPlotOpen,'string'));
saveData = get(handles.txtSaveData,'string');
saveFigure = get(handles.txtSaveFigure,'string');
Type = get(handles.txtType,'string');
Nc =str2num( get(handles.txtNc,'string'));
para = {dt timeLength iint imax n3 alpha deltaf Nc plotopen Type saveData saveFigure}';

check = 0; open = 0;
for p1 = 1 : length(para);    if isempty(para{p1}) == 1       check(p1) = 1;   open = 1;  end;end;

if iint > imax 
    msgbox(' imin 必須小於 imax !','Report','none') 
    set(handles.txtImin,'string',para{3})
elseif open == 1
    msgbox(' 參數設定錯誤,參數必須為數值 !','Report','none') 
elseif strcmp(para{10},'AB')+strcmp(para{10},'CD') ~= 1 
     set(handles.txtType,'string',para{10})  
     msgbox(' Type必須為"AB"或"CD" !','Report','none')      
else
    
 
save('para','para')
msgbox('                   完成 !','Save','none') 
close SSI_ParaSetting_ 
end



